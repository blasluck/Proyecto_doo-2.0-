    private void jlabel_add_UserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlabel_add_UserMouseClicked
    if(!txt_nombres.getText().equals("") & !txt_apellidos.getText().equals("") & !txt_contraseña.getText().equals("") & !jcombo_cargo.getSelectedItem().equals("Seleccionar")){
             Usuarios u = new Usuarios();
             u.setId_usuario(codigo_User(txt_nombres.getText()));
             u.setNombres(txt_nombres.getText());
             u.setApellidos(txt_apellidos.getText());
             u.setCargo(jcombo_cargo.getSelectedItem().toString());
             u.setUsario(usuario_User(u.getNombres(), u.getApellidos(), u.getCargo()));
             u.setContraseña(txt_contraseña.getText());
             metodos_user.add_User(u);
             vaciar();
             listar_Usuarios();
         }else{
             JOptionPane.showMessageDialog(null, "Termine de llenar los campos", "Error!", JOptionPane.ERROR_MESSAGE);
         }
    }//GEN-LAST:event_jlabel_add_UserMouseClicked

