    public String codigo_User(String nom){
       int num= (int) (Math.random()*1000);
       String resultado ="user"+nom.substring(0,3);    
       System.out.println("ID USUARIO"+ resultado);
       return resultado;  
    }
    
    
    public String usuario_User(String nombre, String apellido, String cargo){
        String resultado = nombre.substring(0,3) + apellido_User(apellido)+"@"+cargo.substring(0,3);    
        System.out.println("Resultaod USUARIO"+ resultado);
        return resultado;
    }

    
    private void  listar_Usuarios(){
        modelo_usuarios = metodos_user.listar();
        TABLA_USUARIOS.setModel(modelo_usuarios);
    }
    
    private void vaciar(){
        txt_nombres.setText("");
        txt_apellidos.setText("");
        jcombo_cargo.setSelectedIndex(0);
        txt_contraseña.setText("");
    }

    
    public String apellido_User(String apellido){
        String ap_resultado ="";
   
        for(int i=0; i<apellido.length(); i++){
            if(apellido.charAt(i) != ' '){
                ap_resultado+=apellido.charAt(i);
            }else{
               break;
            }
        }
        
        return ap_resultado;
    }
    
