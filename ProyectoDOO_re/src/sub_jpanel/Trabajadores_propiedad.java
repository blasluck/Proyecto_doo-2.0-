    private void jlabel_edit_trbjMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlabel_edit_trbjMouseClicked
        if(!txt_apellidos.getText().equals("") & !txt_nombre.getText().equals("") & jday_fecha_ingreso.getDate() != null & jday_fecha_nac != null){        
            Trabajador t = new Trabajador();
            t.setId(id_trbjador(txt_apellidos.getText()));
            t.setNombres(txt_nombre.getText());
            t.setApellidos(txt_apellidos.getText());
            t.setCargo(jcombo_cargo.getSelectedItem().toString());
            t.setNacimiento(conversion(jday_fecha_nac.getDate()));
            t.setFecha_ingreso(conversion(jday_fecha_ingreso.getDate()));
            metodos_trbj.update_Trbj(t,codigo_capturado,archivo_img.toString());
            metodos_trbj.listar_Trbj();//Actualizacion de datos
            vaciar();
        }else{  
            JOptionPane.showMessageDialog(null, "Termine de llenar los campos de ingreso", "", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jlabel_edit_trbjMouseClicked

