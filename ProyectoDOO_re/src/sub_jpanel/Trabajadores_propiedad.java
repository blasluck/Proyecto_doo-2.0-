     private void ajustarImagen(JLabel l, String path){
        this.image = new ImageIcon(path);
        this.icono = new ImageIcon(image.getImage().getScaledInstance(l.getWidth(), l.getHeight(), Image.SCALE_DEFAULT));
        l.setIcon(icono);
        this.repaint();
    }
    
