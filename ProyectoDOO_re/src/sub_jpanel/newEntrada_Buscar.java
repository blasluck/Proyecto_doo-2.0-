    private void btn_searchEntradaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_searchEntradaMouseClicked
        if(jday_entrada.getDate() != null){
            DefaultTableModel modelo_entrada = new DefaultTableModel();
            modelo_entrada= metodos_entrada.search_Fecha(conversion(jday_entrada.getDate()));
            if(modelo_entrada.getRowCount()>0){
                TABLA_ENTRADAPRODUCTOS.setModel(modelo_entrada);
            }else{
                JOptionPane.showMessageDialog(null, "No se encontro ninguna entrada durante la fecha: "+conversion(jday_entrada.getDate()) ,"",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ingrese una fecha!" ,"",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_searchEntradaMouseClicked

    
    
    
