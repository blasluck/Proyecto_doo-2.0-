    public void addConatiner(JPanel p,int width, int height,JPanel c){
        p.setSize(width,height);
        p.setLocation(0,0);
        c.removeAll();
        c.add(p,BorderLayout.CENTER);
        c.revalidate();
        c.repaint();
    }
    
    /*OBTENCION DE FECHA ACTUAL*/
    public String fecha_act(){
        Date fecha= new Date();
        SimpleDateFormat formato_fecha = new SimpleDateFormat("yyyy-MM-dd");
        fecha_actual = formato_fecha.format(fecha);
        /**Obtencion de hora actual**/
        LocalTime hora_Actual = LocalTime.now();
        fecha_actual+=" "+hora_Actual;
        return fecha_actual;
    }
    
     
