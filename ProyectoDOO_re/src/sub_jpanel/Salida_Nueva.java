    private void btn_buscar_CodMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_buscar_CodMouseClicked
        if(!txt_buscarcod.equals("")){
            Productos p = new Productos();
            /*Busqueda de producto*/
            p = metodos_salida.search_Prod(txt_buscarcod.getText());
            if(p != null){
                txt_mostrarnombrePord.setText(p.getNombreProducto());
                txt_mostrarAlmacen.setText(valueOf(p.getAlmacen()));
                txt_mostrarCantidadActual.setText(valueOf(p.getCantidad()));
            }else{
                JOptionPane.showMessageDialog(null, "Producto no encontrado");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Termine de llenar el campo!","",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_buscar_CodMouseClicked

    private void btn_searchEmpleadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_searchEmpleadoMouseClicked
        if(!txt_buscarempled.equals("")){
            Trabajador t = new Trabajador();
            /*Busqueda de trabajador*/
            t = metodos_salida.search_Trabj(txt_buscarempled.getText());
            if(t != null){
                txt_mostrarNameEmpl.setText(t.getNombres());
                txt_mostrar_cargo.setText(valueOf(t.getCargo()));
            }else{
                JOptionPane.showMessageDialog(null, "Trabajador no encontrado");
                txt_mostrar_cargo.setText("");
            }
        }else{
           JOptionPane.showMessageDialog(null, "Termine de llenar el campo!","",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_searchEmpleadoMouseClicked

    private void jlabel_agregarsalidaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlabel_agregarsalidaMouseClicked
        if(!txt_buscarcod.getText().equals("") & !txt_buscarempled.getText().equals("") & !txt_cantidad.getText().equals("") & !txt_mostrarAlmacen.getText().equals("")
            & !txt_mostrarCantidadActual.getText().equals("") & !txt_mostrarNameEmpl.getText().equals("") & !txt_mostrar_cargo.getText().equals("") & !txt_mostrarnombrePord.getText().equals("")){
            if(!txt_cantidad.getText().equals("0")){
                if(metodos_salida.cant_limite(txt_buscarcod.getText(), Integer.parseInt(txt_cantidad.getText().toString())) == true){
                    Salida salida = new Salida();
                    salida.setProducto(txt_buscarcod.getText());
                    salida.setTrabajador(txt_buscarempled.getText());
                    salida.setCant_ingreso(Integer.parseInt(txt_cantidad.getText().toString()));
                    salida.setF_Salida(fecha_act());
                    metodos_salida.add_Sistema_salida(salida);
                    JOptionPane.showMessageDialog(null, "Cantidad extraida con exito","",JOptionPane.INFORMATION_MESSAGE);
                    vaciar();
                    metodos_salida.listar_salida();
                }else{
                    JOptionPane.showMessageDialog(null, "La cantidad extraida sobrepasa el limite!","",JOptionPane.ERROR_MESSAGE);
                    txt_cantidad.setText("");
                }  
            }else{
                JOptionPane.showMessageDialog(null, "No puede extraer un valor nulo","",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Termine de llenar los campos!","",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jlabel_agregarsalidaMouseClicked

