    /*Captura de datos*/
    
    public void captura_Datos(String codigo_entrada, Date fecha_ingreso){
       datos_capturados = metodos_entrada.obtencion_Datos(codigo_entrada);
       Productos p = (Productos) datos_capturados[0];
       Trabajador t =  (Trabajador) datos_capturados[1];
       Entrada e = (Entrada) datos_capturados[2];
       /*Datos del producto y entrada capturados*/
       txt_buscarcod.setText(p.getCodigoProducto());
       txt_mostrarnombrePord.setText(p.getNombreProducto());
       txt_mostrarAlmacen.setText(valueOf(p.getAlmacen()));
       txt_mostrarCantidadActual.setText(valueOf(e.getCant_ingreso()));
       txt_mostrarTipo.setText(p.getTipo());

       /*Datos del trabajador capturados*/
       txt_buscarempled.setText(t.getId());
       txt_mostrarNameEmpl.setText(t.getNombres());
       txt_mostrar_rol.setText(t.getCargo());
       
       /*Fecha de ingreso capturada*/
       jday_f_ingreso.setDate(fecha_ingreso);
       
    }
    
    /*OBTENCION DE FECHA ACTUAL*/
    public String fecha_act(Date date){
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String fecha = sdf.format(date);
        System.out.println("FECHA:" + fecha);
        /**Obtencion de hora actual**/
        LocalTime hora_Actual = LocalTime.now();
        fecha+=" "+hora_Actual;
        return fecha;
    }
