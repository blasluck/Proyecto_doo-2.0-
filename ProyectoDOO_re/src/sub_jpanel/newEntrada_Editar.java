    private void btn_searchcodeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_searchcodeMouseClicked
        if(!txt_buscarcod.equals("")){
            Productos p = new Productos();
            /*Busqueda de producto*/
            p = metodos_entrada.search_Prod(txt_buscarcod.getText());
            if(p != null){
                txt_mostrarnombrePord.setText(p.getNombreProducto());
                txt_mostrarAlmacen.setText(valueOf(p.getAlmacen()));
                txt_mostrarCantidadActual.setText("");
                txt_mostrarTipo.setText(p.getTipo());
            }else{
                JOptionPane.showMessageDialog(null, "Producto no encontrado");
                txt_buscarcod.setText("");
                txt_mostrarCantidadActual.setText("");
                txt_mostrarAlmacen.setText("");
                txt_mostrarTipo.setText("");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Termine de llenar el campo!","",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_searchcodeMouseClicked

